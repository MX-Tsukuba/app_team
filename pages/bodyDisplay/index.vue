<template>
  <div class="homebody">
    <div id="tab-container">
      <div class="tab-labels">
        <button
          v-for="(label, index) in tabLabels"
          :key="index"
          class="label"
          :class="{ active: isDisplay === index }"
          @click="selectTab(index)"
          ref="tabButtons"
        >
          <p class="label">{{ label }}</p>
        </button>
        <div class="indicator" :style="indicatorStyle"></div>
      </div>
    </div>

    <div class="gragh">
      <div class="graghChart" v-if="isLoading">データを読み込んでいます</div>
      <div class="graghChart" v-if="!isLoading">
                <ClientOnly>
                    <Line  :data="chartData" :options="chartOptions" />
                </ClientOnly>
      </div>
      <div class="changeWeeks">
        <button style="display: flex; align-items: center;" @click="changeWeeks(-7)">
          <img class="weekBtn reverse" src="~/assets/img/right.png">
          <p class="weeks">前の週</p>
        </button>
        <button style="display: flex; align-items: center;" @click="changeWeeks(7)">
            <p class="weeks">次の週</p>
